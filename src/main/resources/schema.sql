CREATE TABLE IF NOT EXISTS message (
  id BIGSERIAL PRIMARY KEY,
  from_user VARCHAR(100) NOT NULL,
  to_user VARCHAR(100) NOT NULL,
  content TEXT NOT NULL,
  created_at TIMESTAMP NOT NULL,
  read BOOLEAN NOT NULL
);